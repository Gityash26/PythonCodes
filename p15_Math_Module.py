from math import *


# Upper value of a float 
print(f"\nCeil Value : {ceil(3.4)}")



# Lower value of a float 
print(f"\nFloor value : {floor(5.9)}")


# Always return positive value 
print(f"\nAbsolute of -10 : {fabs(-10)}")



# Return the factorial 
print(f"\nfactorial of 5! : {factorial(5)}")



# Return the float SUM of an iterable datatype 
x=(10,20,30,40)
y=[10,20,30,40]
print(f"\nSum of tuple {x} : {fsum(x)}")
print(f"\nSum of list {y} : {fsum(y)}")



# Return the squareroot
print("\nSquare root of 9 : ", sqrt(9))





